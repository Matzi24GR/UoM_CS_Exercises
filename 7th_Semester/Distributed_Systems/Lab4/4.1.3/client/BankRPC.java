import com.rabbitmq.client.AMQP;
import com.rabbitmq.client.ConnectionFactory;
import com.rabbitmq.client.Connection;
import com.rabbitmq.client.Channel;

import java.io.IOException;
import java.util.UUID;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.ExecutionException;
import java.util.concurrent.TimeoutException;

public class BankRPC {

    private Connection connection;
    private Channel channel;
    private String requestQueueName = "rpc_queue";

    public BankRPC() throws IOException, TimeoutException {
        ConnectionFactory factory = new ConnectionFactory();
        factory.setHost("localhost");
        connection = factory.newConnection();
        channel = connection.createChannel();
    }

    public String login(String username, String password) throws IOException, ExecutionException, InterruptedException {
        return call("LOGIN"+username+"####"+password);
    }
    public String withdraw(double amount) throws IOException, ExecutionException, InterruptedException {
        return call("WITHDRAW"+amount);
    }
    public String deposit(double amount) throws IOException, ExecutionException, InterruptedException {
        return call("DEPOSIT"+amount);
    }
    public String balance() throws IOException, ExecutionException, InterruptedException {
        return call("BALANCE");
    }


    private String call(String message) throws IOException, InterruptedException, ExecutionException {
        final String corrId = UUID.randomUUID().toString();

        String replyQueueName = channel.queueDeclare().getQueue();
        AMQP.BasicProperties props = new AMQP.BasicProperties
                .Builder()
                .correlationId(corrId)
                .replyTo(replyQueueName)
                .build();

        channel.basicPublish("", requestQueueName, props, message.getBytes("UTF-8"));

        final CompletableFuture<String> response = new CompletableFuture<>();

        String ctag = channel.basicConsume(replyQueueName, true, (consumerTag, delivery) -> {
            if (delivery.getProperties().getCorrelationId().equals(corrId)) {
                response.complete(new String(delivery.getBody(), "UTF-8"));
            }
        }, consumerTag -> {
        });

        String result = response.get();
        channel.basicCancel(ctag);
        return result;
    }
}
